package classes;

public class CadastroAssociados {
    private String nome;
    private float cpf;
    private float rg;
    private float telefone;
    private String dataNasc;
    private String email;
    private String infoFinanceiras;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getCpf() {
        return cpf;
    }

    public void setCpf(float cpf) {
        this.cpf = cpf;
    }

    public float getRg() {
        return rg;
    }

    public void setRg(float rg) {
        this.rg = rg;
    }

    public float getTelefone() {
        return telefone;
    }

    public void setTelefone(float telefone) {
        this.telefone = telefone;
    }

    public String getDataNasc() {
        return dataNasc;
    }

    public void setDataNasc(String dataNasc) {
        this.dataNasc = dataNasc;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getInfoFinanceiras() {
        return infoFinanceiras;
    }

    public void setInfoFinanceiras(String infoFinanceiras) {
        this.infoFinanceiras = infoFinanceiras;
    }
}
